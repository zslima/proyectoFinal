    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String categ = txtcategoria.getText();
    
        int op = cdao.registrarCategoria(categ);
        limpiar();
        if(op!=0){
            JOptionPane.showMessageDialog(null,"Categoria registrado!");
            updateComponets();
            btnAgregar.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null,"Categoria no registrado!");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tbCategoria.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "Seleccionar Categoria a ELIMINAR");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea ELIMINAR el Usuario? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int celda = (int) tbCategoria.getValueAt(fila, 0);
                int x = cdao.eliminarCategoria(celda);
                if(x==1){
                    JOptionPane.showMessageDialog(null, "Categoria ELIMINADO!");
                    updateComponets();
                    limpiar();
                }else{
                    JOptionPane.showMessageDialog(null, "Categoria no ELIMINADO!");
                }
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fil = tbCategoria.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Categoria a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR el Producto? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int id = Integer.parseInt(txtidCategoria.getText());
                String n = txtcategoria.getText();
                
                int x = cdao.modificarCategoria(id,n);

                if(x==1){
                    JOptionPane.showMessageDialog(null, "Categoria MODIFICADO!");
                    updateComponets();
                    limpiar();

                }else{
                    JOptionPane.showMessageDialog(null, "Categoria no se ha MODIFICADO!");
                }
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void tbCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCategoriaMouseClicked
        if(evt.getButton()==1){
            int fila = tbCategoria.getSelectedRow();
            int celda = (int) tbCategoria.getValueAt(fila, 0);
            lista = cdao.listarCategoria(celda);
            for(int i=0;i<lista.size();i++){
                
                txtidCategoria.setText(""+lista.get(i).getIdCategoria());
                txtcategoria.setText(lista.get(i).getNombreCat());
               

            }
        }
    }//GEN-LAST:event_tbCategoriaMouseClicked

