void limpiar(){
txtidCategoria.setText(null);
txtcategoria.setText(null);


}
final void listarCategoria(){
    lista = cdao.listarCategoria();
    model = (DefaultTableModel) tbCategoria.getModel();
        Object[] cat = new Object[3];
        for(int i=0;i<lista.size();i++){
           
            cat[0]=lista.get(i).getIdCategoria();
            cat[1]=lista.get(i).getNombreCat();
            
            model.addRow(cat);
        }        
        tbCategoria.setModel(model);
}
void LimpiarTabla(DefaultTableModel modelo){
        int a =modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){  
            modelo.removeRow(i);
        }
 }

 void updateComponets(){
            LimpiarTabla(model);
            listarCategoria();
            modelocombo.removeAllElements();
            modelolista.clear();
            cargarList(); 
    }
