    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tbproducto.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Usuario a ELIMINAR");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea ELIMINAR el Usuario? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int celda = (int) tbproducto.getValueAt(fila, 0);
                int x = pdao.eliminarProducto(celda);
                if(x==1){
                    JOptionPane.showMessageDialog(null, "Usuario ELIMINADO!");
                    updateComponets();
                    limpiar();
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario no ELIMINADO!");
                }
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fil = tbproducto.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Producto a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR el Producto? ");
            if(JOptionPane.OK_OPTION==confirmar) {
                int id = Integer.parseInt(txtidProducto.getText());
                int idc = Integer.parseInt(txtidcategoria.getText());
                String p = txtproducto.getText();
                int stc = Integer.parseInt(txtstock.getText());
                double pr = Double.parseDouble(txtprecio.getText());
                int x = pdao.modificarProducto(id,idc, p, stc,pr);
                
                if(x==1){
                    JOptionPane.showMessageDialog(null, "Producto MODIFICADO!");
                    updateComponets();
                    limpiar();
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Producto no se ha MODIFICADO!");
                }
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void tbproductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbproductoMouseClicked
          if(evt.getButton()==1){
            int fila = tbproducto.getSelectedRow();
            int celda = (int) tbproducto.getValueAt(fila, 0);
            lista1 = pdao.listarProducto(celda);
            for(int i=0;i<lista1.size();i++){
                txtidcategoria.setText(""+lista1.get(i).getIdc());
                txtidProducto.setText(""+lista1.get(i).getIdp());
                txtproducto.setText(lista1.get(i).getNombrep());
                txtstock.setText(""+lista1.get(i).getStock());
                txtprecio.setText(""+lista1.get(i).getPrecio());
               
            }
        }        
    }//GEN-LAST:event_tbproductoMouseClicked
final void cargarCategoria(){
 lista = cdao.listarCategoria();
 cboCategoria.setModel(modelocombo);
 modelocombo.addElement("Cargar Categoria");
 for(int i=0;i<lista.size();i++){
     modelocombo.addElement(lista.get(i).getNombreCat());
 }
cboCategoria.setModel(modelocombo);
}
void limpiar(){
txtidcategoria.setText(null);
txtproducto.setText(null);
txtidProducto.setText(null);
txtprecio.setText(null);
txtstock.setText(null);
cboCategoria.setSelectedIndex(0);

}
final void listarProducto(){
    lista1 = pdao.listarProducto();
    model = (DefaultTableModel) tbproducto.getModel();
        Object[] user = new Object[5];
        for(int i=0;i<lista1.size();i++){
            user[0]=lista1.get(i).getIdp();
            user[1]=lista1.get(i).getIdc();
            user[2]=lista1.get(i).getNombrep();
            user[3]=lista1.get(i).getPrecio();
            user[4]=lista1.get(i).getStock();
            model.addRow(user);
        }        
        tbproducto.setModel(model);
}
void LimpiarTabla(DefaultTableModel modelo){
        int a =modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){  
            modelo.removeRow(i);
        }
 }

